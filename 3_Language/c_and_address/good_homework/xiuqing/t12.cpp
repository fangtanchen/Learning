#include <stdio.h>
int main()
{
	float x,y,a,b,c,d,h=0;
	printf("请输入坐标:\n");
	scanf("(%f,%f",&x,&y);
	a=(x-2.0)*(x-2.0)+(y-2.0)*(y-2.0);
	b=(x+2.0)*(x+2.0)+(y-2.0)*(y-2.0);
	c=(x+2.0)*(x+2.0)+(y+2.0)*(y+2.0);
	d=(x-2.0)*(x-2.0)+(y+2.0)*(y+2.0);
	if(a<=1.0||b<=1.0||c<=1.0||d<=1.0)
		h=10.0;
	printf("该点建筑高度为%f米\n",h);
	return 0;
}
